package com.nw.util;

public class HoraCadena {

	private int hora ;
	private int min ; 
	private String horaTexto ;
	private String horaMinutoTexto;
	
	
	public HoraCadena() {
	}

	public int getHora() {
		return hora;
	}

	public void setHora(int hora) {
		this.hora = hora;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public String getHoraTexto() {
		return horaTexto;
	}

	public void setHoraTexto(String horaTexto) {
		this.horaTexto = horaTexto;
	}

	public String getHoraMinutoTexto() {
		return horaMinutoTexto;
	}

	public void setHoraMinutoTexto(String horaMinutoTexto) {
		this.horaMinutoTexto = horaMinutoTexto;
	}


	
}
